package models;


/**
 *
 * @author puelon
 */
public class Menu {
    
    private int id;
    private String nome;
    private String preco;
    private String tipo;

     public Menu(int id, String nome, String preco, String tipo) {
      this.id = id;
      this.nome = nome;
      this.preco = preco;
      this.tipo = tipo;
    }
     public Menu(){
         
     }
    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        int oldId = this.id;
        this.id = id;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the preco
     */
    public String getPreco() {
        return preco;
    }

    /**
     * @param preco the preco to set
     */
    public void setPreco(String preco) {
        this.preco = preco;
    }

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo){
        this.tipo = tipo;
    }
    
}
